/*
Sum of Numbers With Units Digit K

You are given two integers, num and k. You need to find the 
minimum possible size of a set of positive integers with the following properties:

Each integer in the set has the unit digit equal to k.
The sum of all the integers in the set equals num.
If such a set exists, return its minimum size. Otherwise, if no set satisfies the conditions, return -1.

Note:
The set can contain multiple instances of the same integer,
 and if the set is empty, its sum is considered to be 0.
The unit digit of a number refers to its rightmost digit.
Consider the input:

num = 58
k = 9

The function should return 2 because there exists a valid set with two positive integers,
 both having the units digit 9, and their sum is equal to 58.

Important Note: Ensure that you save your solution before progressing to 
the next question and before submitting your answer.

Exercise-1

Input :
56
9

Output :
4

Exercise-2

Input:
53

Output:
-1
*/
#include <iostream>
using namespace std;

int minimumNumbers(int num, int k) {
    if (num == 0) return 0;
    for (int i = 1; i <= num; ++i) {
        int t = num - k * i;
        if (t >= 0 && t % 10 == 0) return i;
    }
    return -1;
}

int main() {
    int num,k;
    cin >> num >> k;
    int result = minimumNumbers(num,k);
    cout << result << endl;

    return 0;
}
